package back.exam04;

public class Ex08 {

    public static void main(String[] args) {

        // 개념이해 잘해야함

        /*
         * 반복문 : for, while, do~ while
         * -무한루프 (안좋아)
         * (초기값=초기화)
         * 
         * for (초기값; 종료할조건; 증가치) {
         * }
         */

        // 1~10까지 콘솔에 찍기
        for (int i = 1; i < 11; i = i + 1) {
            System.out.println(i);
        }

    }
}
